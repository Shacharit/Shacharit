package com.google.face2face.backend.utils;

import java.util.ArrayList;
import java.util.List;

public class ListUtils {
    public <T> List<T> intersection(List<T> list1, List<T> list2) {
        List<T> list = new ArrayList<T>();

        for (T t : list1) {
            if(list2.contains(t)) {
                list.add(t);
            }
        }

        return list;
    }
}
